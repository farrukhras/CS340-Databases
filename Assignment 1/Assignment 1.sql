 from SCHOOLS INNER JOIN CENSUS_DATA
 on SAFETY_SCORE = 1 and  SCHOOLS.COMMUNITY_AREA_NUMBER =  CENSUS_DATA.COMMUNITY_AREA_NUMBER;
 